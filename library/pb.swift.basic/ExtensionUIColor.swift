import Foundation
import UIKit

extension UIColor
{
    //黑灰色系
    //象牙黑（#292421）
    class func ivoryBlackColor() -> UIColor
    {
        return UIColor(red: 41/255, green: 36/255, blue: 33/255, alpha: 1)
